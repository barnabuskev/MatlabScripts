% --- Executes on button press 'Done'.
function done_Callback(hObject, eventdata, handles)
% hObject    handle to browse (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
chan_info = {};
popups = findobj('Style','popupmenu');
samp_freq = 135;
for i = 1:size(popups,1)
    %read popup menu data
    sel = get(popups(i),'Value');
    strings = get(popups(i),'String');
    if sel ~= 1
        %adjust sampling rate
        switch strings{sel,1}
            case 'EMG'
                if samp_freq < 8100
                    samp_freq = 8100;
                end
            case 'Heart Sounds'
                samp_freq = 15000;
        end
        %load chan_info with channel no. & label
        tg = get(popups(i),'Tag');
        if isempty(chan_info)
            chan_info = {str2num(tg(10:end))-1,strings{sel,1}};
        else
            chan_info = [chan_info;{str2num(tg(10:end))-1,strings{sel,1}}];
        end
    end
end
%save channel information & sampling frequency
if ~isempty(chan_info)
    handles.chan_info=chan_info;
    handles.samp_freq=samp_freq;
    DataGraph('Go',chan_info,samp_freq,get(handles.edit1,'String'));
    guidata(hObject,handles);
    close(handles.DL2000);
else
    msgbox('You have not selected any sensors','Message from setup','warn') 
end





